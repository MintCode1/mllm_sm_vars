"""Generate phase-level SM/TPC plots from SM variation experiment CSV.

This script refuses to generate plots unless the paired validation sidecar file
marks extreme SM masking as passed.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import matplotlib.pyplot as plt
import pandas as pd


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--input-csv",
        type=Path,
        default=Path("experiment_outputs/sm_phase_results.csv"),
        help="CSV generated by run_sm_variation_phase_experiment.py",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("experiment_outputs/sm_phase_plots"),
        help="Directory for generated plot PNG files",
    )
    parser.add_argument(
        "--include-total",
        action="store_true",
        help="Also generate optional total latency/throughput/power plots",
    )
    return parser.parse_args()


def ensure_validation_passed(input_csv: Path) -> None:
    validation_path = input_csv.with_suffix(".validation.json")
    if not validation_path.exists():
        raise FileNotFoundError(
            "Missing validation sidecar. Run the phase experiment script first "
            "and pass extreme validation before plotting."
        )
    data = json.loads(validation_path.read_text(encoding="utf-8"))
    if not data.get("passed", False):
        raise RuntimeError(
            "Validation sidecar indicates masking validation did not pass. "
            "Plots are blocked to avoid invalid SM sweep analysis."
        )


def make_plot(df: pd.DataFrame, y_col: str, y_label: str, title: str, output: Path) -> None:
    plt.figure(figsize=(8.2, 5.0))
    plt.plot(df["active_tpcs"], df[y_col], marker="o", linewidth=2.0)
    plt.xlabel("Active TPCs")
    plt.ylabel(y_label)
    plt.title(title)
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.savefig(output, dpi=220)
    plt.close()


def main() -> None:
    args = parse_args()
    if not args.input_csv.exists():
        raise FileNotFoundError(f"Missing CSV: {args.input_csv}")

    ensure_validation_passed(args.input_csv)

    args.output_dir.mkdir(parents=True, exist_ok=True)
    df = pd.read_csv(args.input_csv).sort_values(["active_tpcs", "active_sms"])

    make_plot(
        df,
        y_col="encoder_latency",
        y_label="Encoder Latency (s/request)",
        title="Encoder Latency vs SM/TPC",
        output=args.output_dir / "01_encoder_latency_vs_smtpc.png",
    )
    make_plot(
        df,
        y_col="encoder_throughput",
        y_label="Encoder Throughput (req/s)",
        title="Encoder Throughput vs SM/TPC",
        output=args.output_dir / "02_encoder_throughput_vs_smtpc.png",
    )
    make_plot(
        df,
        y_col="encoder_power",
        y_label="Encoder Power (W)",
        title="Encoder Power vs SM/TPC",
        output=args.output_dir / "03_encoder_power_vs_smtpc.png",
    )

    make_plot(
        df,
        y_col="prefill_latency",
        y_label="Prefill Latency (s/request)",
        title="Prefill Latency vs SM/TPC",
        output=args.output_dir / "04_prefill_latency_vs_smtpc.png",
    )
    make_plot(
        df,
        y_col="prefill_throughput",
        y_label="Prefill Throughput (tok/s)",
        title="Prefill Throughput vs SM/TPC",
        output=args.output_dir / "05_prefill_throughput_vs_smtpc.png",
    )
    make_plot(
        df,
        y_col="prefill_power",
        y_label="Prefill Power (W)",
        title="Prefill Power vs SM/TPC",
        output=args.output_dir / "06_prefill_power_vs_smtpc.png",
    )

    make_plot(
        df,
        y_col="decode_latency",
        y_label="Decode Latency (s/request)",
        title="Decode Latency vs SM/TPC",
        output=args.output_dir / "07_decode_latency_vs_smtpc.png",
    )
    make_plot(
        df,
        y_col="decode_throughput",
        y_label="Decode Throughput (tok/s)",
        title="Decode Throughput vs SM/TPC",
        output=args.output_dir / "08_decode_throughput_vs_smtpc.png",
    )
    make_plot(
        df,
        y_col="decode_power",
        y_label="Decode Power (W)",
        title="Decode Power vs SM/TPC",
        output=args.output_dir / "09_decode_power_vs_smtpc.png",
    )

    if args.include_total:
        make_plot(
            df,
            y_col="total_latency",
            y_label="Total Latency (s/request)",
            title="Total Latency vs SM/TPC",
            output=args.output_dir / "10_total_latency_vs_smtpc.png",
        )
        make_plot(
            df,
            y_col="total_throughput",
            y_label="Total Throughput (tok/s)",
            title="Total Throughput vs SM/TPC",
            output=args.output_dir / "11_total_throughput_vs_smtpc.png",
        )
        make_plot(
            df,
            y_col="total_power",
            y_label="Total Power (W)",
            title="Total Power vs SM/TPC",
            output=args.output_dir / "12_total_power_vs_smtpc.png",
        )

    print(f"Wrote plots to: {args.output_dir}")


if __name__ == "__main__":
    main()
